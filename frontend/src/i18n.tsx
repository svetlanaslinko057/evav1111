export const DICTS: Record<LangCode, Dict> = { en: EN, uk: UK };

// Reverse index: EN literal → key. Built once per process. Lets call-sites
// translate hard-coded English strings without manually picking the key:
//   <Text>{tByEn('Save')}</Text>
// Whitespace is normalised so multi-line JSX literals match too.
export const normEn = (s: string) => (typeof s === 'string' ? s.trim().replace(/\s+/g, ' ') : s);
export const EN_REVERSE: Record<string, string> = (() => {
  const out: Record<string, string> = {};
  for (const [key, val] of Object.entries(EN)) {
    if (typeof val !== 'string') continue;
    const k = normEn(val);
    // First-wins so the most-canonical key for a given literal sticks.
    if (!(k in out)) out[k] = key;
  }
  return out;
})();

// ─── Module-level mirror of the active language ─────────────────────────────
// Lets non-hook code paths (imperative APIs like `Alert.alert`, helpers called
// outside React render) translate strings without subscribing to context.
// Kept in sync with `I18nProvider`'s setLang.
let _CURRENT_LANG: LangCode = 'en';
const _LANG_LISTENERS = new Set<(l: LangCode) => void>();

export function getCurrentLang(): LangCode {
  return _CURRENT_LANG;
}

export function subscribeLang(fn: (l: LangCode) => void): () => void {
  _LANG_LISTENERS.add(fn);
  return () => { _LANG_LISTENERS.delete(fn); };
}

function _setCurrentLang(next: LangCode) {
  if (_CURRENT_LANG === next) return;
  _CURRENT_LANG = next;
  _LANG_LISTENERS.forEach((fn) => { try { fn(next); } catch { /* swallow */ } });
}

/**
 * Translate by English literal using the module-level current language.
 * Safe to call from anywhere (non-hook). Returns input unchanged if there
 * is no translation or the active language is English.
 */
export function translateByEn(en: string): string {
  if (typeof en !== 'string') return en;
  const lang = _CURRENT_LANG;
  if (lang === 'en') return en;
  const key = EN_REVERSE[normEn(en)];
  if (!key) return en;
  const dict = DICTS[lang] || EN;
  return dict[key] ?? en;
}

type I18nCtx = {
  lang: LangCode;
  setLang: (next: LangCode, opts?: { syncBackend?: boolean }) => Promise<void>;
  t: (key: string, fallback?: string) => string;
  tByEn: (en: string) => string;
  langs: typeof LANGS;
};

const I18nContext = createContext<I18nCtx>({
  lang: 'en',
  setLang: async () => {},
  t: (k, fb) => fb || k,
  tByEn: (en) => en,
  langs: LANGS,
});

export function I18nProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<LangCode>('en');

  // Hydrate from AsyncStorage on mount.
  useEffect(() => {
    (async () => {
      try {
        const v = await AsyncStorage.getItem(STORAGE_KEY);
        if (v === 'ru') {
          // Russian was removed — migrate any previously-persisted choice to Ukrainian.
          await AsyncStorage.setItem(STORAGE_KEY, 'uk');
          setLangState('uk');
          _setCurrentLang('uk');
        } else if (v === 'en' || v === 'uk') {
          setLangState(v);
          _setCurrentLang(v);
        }
      } catch {/* ignore */}
    })();
  }, []);

  const setLang = useCallback(async (next: LangCode, opts?: { syncBackend?: boolean }) => {
    setLangState(next);
    _setCurrentLang(next);
    try { await AsyncStorage.setItem(STORAGE_KEY, next); } catch {/* ignore */}
    if (opts?.syncBackend !== false) {
      // Best-effort: push preference to backend so it follows the account.
      try { await api.patch('/account/me', { language: next }); } catch {/* unauth or not yet wired — fine */}
    }
  }, []);

  const t = useCallback((key: string, fallback?: string) => {
    const dict = DICTS[lang] || EN;
    return dict[key] ?? EN[key] ?? fallback ?? key;
  }, [lang]);

  // tByEn — mirror of the web `tByEn` pattern. Translate by English literal
  // lookup. Returns the input unchanged if no match (so hardcoded EN strings
  // remain readable on en, and any unknown literal degrades gracefully).
  const tByEn = useCallback((en: string) => {
    if (typeof en !== 'string') return en;
    if (lang === 'en') return en;
    const key = EN_REVERSE[normEn(en)];
    if (!key) return en;
    const dict = DICTS[lang] || EN;
    return dict[key] ?? en;
  }, [lang]);

  const value = useMemo<I18nCtx>(
    () => ({ lang, setLang, t, tByEn, langs: LANGS }),
    [lang, setLang, t, tByEn],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useT() {
  return useContext(I18nContext);
}
